let n = +prompt("숫자입력");
let n_name = [];
let n_score = [];
for (let i = 0; i < n; i++) {
  n_name.push(prompt("이름입력"));
  n_score.push(prompt(n_name[i] + "점수 입력"));
  n_name[i] === n_score[i];
}

